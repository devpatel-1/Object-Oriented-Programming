public class pr2_13 {
    public static void main(String[] args) {

        int x = 5;

        System.out.println(++x); // Output: 6 (x becomes 6, then printed)

        System.out.println(x--); // Output: 6 (x is printed as 6, then decremented to 5)

        System.out.println(x);  // Output: 5 (x is now 5 after the previous operation)

        System.out.println(--x); // Output: 4 (x becomes 4, then printed)

        System.out.println(x++); // Output: 4 (x is printed as 4, then incremented to 5)

        System.out.println(x);  // Output: 5 (x is now 5 after the previous operation)

    }
}

